    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        if (tabelaResultados.getSelectedRow() >= 0) {
            final int row = tabelaResultados.getSelectedRow();
            String nome = (String) tabelaResultados.getValueAt(row, 1);
            int resposta = JOptionPane.showConfirmDialog(rootPane,
                    "Excluir o Cliente \"" + nome + "\"?",
                    "Confirmar exclusÃ£o", JOptionPane.YES_NO_OPTION);
            if (resposta == JOptionPane.YES_OPTION) {
                try {
                    Integer id = (Integer) tabelaResultados.getValueAt(row, 0);
                    ServicoCliente.excluirCliente(id);
                    this.refreshList();
                } catch (Exception e) {
                    e.printStackTrace();
                    JOptionPane.showMessageDialog(rootPane, e.getMessage(),
                            "Falha na ExclusÃ£o", JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "VocÃª nÃ£o selecionou ninguÃ©m.");
        }
    }//GEN-LAST:event_jButton6ActionPerformed

    private void Label_PesquisaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Label_PesquisaMouseClicked
        if (evt.getClickCount() == 1) {

            boolean resultSearch = false;
            ultimaPesquisa = campo_Pesquisa.getText();

            try {
                resultSearch = refreshList();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, e.getMessage(),
                        "Falha ao obter lista", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (!resultSearch) {
                JOptionPane.showMessageDialog(rootPane, "A pesquisa nÃ£o retornou resultados ",
                        "Sem resultados", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_Label_PesquisaMouseClicked

    private void visualizarDadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_visualizarDadosActionPerformed
        try {
            final int row = tabelaResultados.getSelectedRow();
            if (row >= 0) {
                Integer id = (Integer) tabelaResultados.getValueAt(row, 0);

                Cliente cliente = ServicoCliente.obterCliente(id);

                visu.dispose();
                visu = new VisualizarCli();
                visu.setCliente(cliente);
                visu.setTitle(cliente.getNome() + " ");
                this.getParent().add(visu);
                this.openFrameInCenter(visu);
                visu.toFront();
            } else {
                JOptionPane.showMessageDialog(rootPane, "VocÃª nÃ£o selecionou ninguÃ©m.");
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, "NÃ£o Ã© possÃ­vel "
                    + "exibir os detalhes deste cliente.",
                    "Erro ao abrir detalhe", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_visualizarDadosActionPerformed

