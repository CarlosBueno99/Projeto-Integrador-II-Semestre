    public boolean refreshList() throws ProdutoException, Exception {
        if (Nome_button.isSelected() || !Nome_button.isSelected() ) {//pesquisa atravÃ©s do parametro NOME
            List<Cliente> resultado = ServicoCliente.
                    procurarCliente(ultimaPesquisa);
            DefaultTableModel model = (DefaultTableModel) tabelaResultados.getModel();
            model.setRowCount(0);
            if (resultado == null || resultado.size() <= 0) {
                return false;
            }
            for (int i = 0; i < resultado.size(); i++) {
                Cliente cli = resultado.get(i);
                if (cli != null) {
                    Object[] row = new Object[6];
                    row[0] = cli.getId();
                    row[1] = cli.getNome();
                    row[2] = cli.getCPF();
                    row[3] = cli.getGenero();
                    row[4] = cli.getEndereco();
                    row[5] = cli.getTelefone_casa();
                    model.addRow(row);
                }
            }
            return true;
        } else if (CPF_Button.isSelected()) {//pesquisa atravÃ©s do parametro CPF
            List<Cliente> resultado = ServicoCliente.
                    procurarClienteCPF(ultimaPesquisa);
            DefaultTableModel model = (DefaultTableModel) tabelaResultados.getModel();
            model.setRowCount(0);
            if (resultado == null || resultado.size() <= 0) {
                return false;
            }
            for (int i = 0; i < resultado.size(); i++) {
                Cliente cli = resultado.get(i);
                if (cli != null) {
                    Object[] row = new Object[6];
                    row[0] = cli.getId();
                    row[1] = cli.getNome();
                    row[2] = cli.getCPF();
                    row[3] = cli.getGenero();
                    row[4] = cli.getEndereco();
                    row[5] = cli.getTelefone_casa();
                    model.addRow(row);
                }
            }
            return true;
        }
        return true;
    }
